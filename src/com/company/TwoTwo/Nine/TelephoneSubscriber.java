package com.company.TwoTwo.Nine;

public class TelephoneSubscriber {
    private String Name;
    private String SecondName;
    private String Patronymic;
    private String Address;
    private String City;
    private String Pasport;

    public TelephoneSubscriber(String name, String secondName, String patronymic, String address, String city, String pasport) {
        Name = name;
        SecondName = secondName;
        Patronymic = patronymic;
        Address = address;
        City = city;
        Pasport = pasport;
    }
}
